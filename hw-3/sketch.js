// Initiate setup function
function setup() {
    
    // Create a canvas that is 500 pixels wide
    // and 500 pixels high
    createCanvas( 500, 500 )
    
    // Color the background Spring Green
    // using RGB color scale
    background( 0, 255, 127 )
}

// Initiate draw function
function draw() {
    
    // Tell p5 to write: Hello World!
    // at coordinates: x-215 and y-250
    text("Hello World!", 215, 250)
}
